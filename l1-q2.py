n1, n2 = [int(x) for x in input("Insira os números separados por vírgula: ").split(", ")]
summation = n1 + n2
subtraction = n1 - n2
product = n1 * n2
division = n1 / n2
print(f"O valor final das operações são: soma = {summation}, subtração = {subtraction}, produto = {product}, divisão = {division}")